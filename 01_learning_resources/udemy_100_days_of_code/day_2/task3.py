# Day 2 of 100 Days of Code Practice File - Mathematical Operations

print(456 + 789) # Addition
print(456 - 789) # Subtraction
print(456 * 789) # Multiplication  
print(456 / 789) # Division - always results in a float
print(456 // 789) # Always esults in an integer by rounding down the result to the nearest whole number, it simply drops all the decimal places and gives you the whole number part of the result so be careful!
print(456 ** 2) # Exponent - raises the first number to the power of the second number
print(7 % 3) # Modulo - gives you the remainder of the division of the first number by the second number
