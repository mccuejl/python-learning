# Check if the number input is odd or even using the modulo operator and conditional checks
print("Welcome to the odd/even number checker.")
number_to_check = int(input("Please enter a number to check whether it is an odd or even number. \n"))

if number_to_check % 2 == 0:
    print("You entered an even number.")
else:
    print("You entered an odd number.")

    
